export function filterGeospatialCopImpactedAssetsBySector(
  assets: GeospatialCopImpactedAssetRecord[],
  sectors: GeospatialCopImpactedAssetSector[]
): GeospatialCopImpactedAssetRecord[] {
  if (sectors.length === 0) {
    return assets
  }
  return assets.filter((asset) => sectors.includes(asset.sector))
}
