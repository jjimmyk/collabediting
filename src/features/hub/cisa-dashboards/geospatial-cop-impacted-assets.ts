const COP_WORKSPACE_LABEL = 'Port of Houston Outage'

function asset(
  record: Omit<
    GeospatialCopImpactedAssetRecord,
    | 'deploymentKind'
    | 'assignedWorkspaceId'
    | 'assignedWorkspaceKind'
    | 'assignedIncidentName'
    | 'assignedExerciseName'
    | 'orgChartReportsTo'
    | 'orgChartSortOrder'
    | 'ics204DocumentId'
    | 'pointOfContactMemberId'
    | 'assetCheckInStatus'
    | 'competencyFunction'
    | 'areaKey'
  > & { areaKey?: GeospatialCopImpactedAssetRecord['areaKey'] }
): GeospatialCopImpactedAssetRecord {
  return {
    deploymentKind: 'incident',
    assignedWorkspaceId: null,
    assignedWorkspaceKind: null,
    assignedIncidentName: COP_WORKSPACE_LABEL,
    assignedExerciseName: null,
    orgChartReportsTo: null,
    orgChartSortOrder: 0,
    ics204DocumentId: null,
    pointOfContactMemberId: null,
    assetCheckInStatus: null,
    competencyFunction: null,
    areaKey: record.areaKey ?? 'atlantic',
    ...record,
  }
}

